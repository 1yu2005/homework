'''
这是一个递归解法，利用数学推导简化了模拟过程：

递归思想
基本情况：当只有1个人时（n=1），最后留下的就是第1号

递归关系：当有n个人时，最后留下的人的位置可以通过n-1个人的结果推算出来

数学推导
假设我们知道n-1个人时的幸存者位置为 func(n-1)（从0开始计数），那么n个人时的幸存者位置为：

text
(func(n-1) + 2) % n + 1
为什么是 +2 和 % n？

第一轮报数中，报到3的人（第3号）会被淘汰

从第4号开始新一轮报数

相当于把问题规模从n缩小到n-1，但起始位置偏移了3位

公式中的 +2 代表偏移量（因为报到3的人出局，从下一个人重新开始）

% n 确保位置在有效范围内（0到n-1）

最后 +1 是因为代码中位置从1开始计数，而计算中使用的是0基计数
'''

'''
目：有n个人围成一圈，顺序排号。
从第一个人开始报数（从1到3报数），凡报到3的人退出圈子，问最后留下的是原来第几号的那位。
'''

def func(n):
    if n == 1:
        return 1
    else:
        return (func(n - 1) + 2) % n + 1

if __name__ == "__main__":
    n = int(input("请输入你要围成的人数："))
    print(f"最后留下的是原来第{func(n)}号的那位")
