'''
题目：猴子吃桃问题：猴子第一天摘下若干个桃子，当即吃了一半，还不瘾，又多吃了一个   
第二天早上又将剩下的桃子吃掉一半，又多吃了一个。以后每天早上都吃了前一天剩下的一半零一个。
到第10天早上想再吃时，见只剩下一个桃子了。求第一天共摘了多少。  

程序分析：采取逆向思维的方法，从后往前推断
'''

def monkey_peach():
    """逆向推导：从第10天往前推"""
    peach = 1  # 第10天早上剩下的桃子
    
    # 从第9天推到第1天（共推9次）
    for day in range(9, 0, -1):
        peach = (peach + 1) * 2
        print(f"第{day}天开始前有 {peach} 个桃子")
    
    return peach

total = monkey_peach()
print(f"\n第一天共摘了 {total} 个桃子")
